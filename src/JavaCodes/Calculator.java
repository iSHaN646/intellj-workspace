package JavaCodes;
class Calculator 
{
  
	public int add(int a,int b){
		return a+b;
	}

	public int subt(int a,int b){
		return a-b;
	}

	public int mult(int a,int b){
		return a*b;
	}

	public int div(int a,int b){
		return a/b;
	}

	public int modulo(int a,int b){
		return a%b;
	}

	public double sqrt(int a){
		return Math.sqrt(a);
	}

	public double pow(int a,int b){
		return Math.pow(a,b);
	}
}
